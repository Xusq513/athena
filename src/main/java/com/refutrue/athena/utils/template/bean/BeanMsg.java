package com.refutrue.athena.utils.template.bean;

public class BeanMsg {

	private String author;
	
	private String date;
	
	private String beanName;
	
	private String beanSimpleName;
	
	private String firstLetterBeanName;
	
	private String title;
	
	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getBeanName() {
		return beanName;
	}

	public void setBeanName(String beanName) {
		this.beanName = beanName;
	}


	public String getBeanSimpleName() {
		return beanSimpleName;
	}

	public void setBeanSimpleName(String beanSimpleName) {
		this.beanSimpleName = beanSimpleName;
	}

	public String getFirstLetterBeanName() {
		return firstLetterBeanName;
	}

	public void setFirstLetterBeanName(String firstLetterBeanName) {
		this.firstLetterBeanName = firstLetterBeanName;
	}
	
	
}
